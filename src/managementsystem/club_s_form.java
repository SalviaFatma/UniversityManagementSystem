    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        try{
            Class.forName("java.sql.DriverManager");
            Connection conn = (Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/universitymanagementsystem", "root" , "");
            Statement st = (Statement)conn.createStatement();
            String sql = "INSERT INTO `club_s_form` VALUES (? ,? ,? ,? ,? ,? ,? ,? ,? ,? ,? ,? ,? ,? ,? ,? ,? ,? )";
            
            PreparedStatement ptstat = conn.prepareStatement(sql);
            ptstat.setString(1,name.getText());
            ptstat.setString(2,roll.getText() );
            ptstat.setString(3, sem.getText());
            ptstat.setString(4,branch.getText() );
            ptstat.setBoolean(5, ieee.isSelected());
            ptstat.setBoolean(6, gfg.isSelected());
            ptstat.setBoolean(7, dance.isSelected());
            ptstat.setBoolean(8, music.isSelected());
            ptstat.setBoolean(9, drama.isSelected());
            ptstat.setBoolean(10, literary.isSelected());
            ptstat.setBoolean(11, photo.isSelected());
            ptstat.setBoolean(12, ed.isSelected());
            ptstat.setBoolean(13, uba.isSelected());
            ptstat.setBoolean(14, nss.isSelected());
            ptstat.setBoolean(15, coding.isSelected());
            ptstat.setBoolean(16, dristi.isSelected());
            ptstat.setBoolean(17, codechef.isSelected());
            ptstat.setBoolean(18, robotics.isSelected());
            
            ptstat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Saved");
            conn.close();
            
        }
        catch( Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

